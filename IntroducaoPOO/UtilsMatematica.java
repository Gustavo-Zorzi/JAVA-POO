package IntroducaoPOO;

public class UtilsMatematica {
	
	public static void geraOperacoesBasicas(double dNumero1, double dNumero2) {
		double dSoma = dNumero1 + dNumero2;
		System.out.println(dSoma);
		double dSubtracao = dNumero1 - dNumero2;
		System.out.println(dSubtracao);		
		double dMultiplicao = dNumero1*dNumero2;
		System.out.println(dMultiplicao);				
		double dDivisao = dNumero1/dNumero2;
		System.out.println(dDivisao);				
	}
}
